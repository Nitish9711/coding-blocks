package Basics;
public class DataTypesTutorial {
    public static void main(String[] args) {
        byte x = 2; // most significatnt bit is sign bit thats why byte range is -2^7 to 2^7-1 though it has 8 bits
        System.out.println(x);
        float rateOfInterest = 2.3f; // f is necessary here;
        double rate = 3.1428571;
        System.out.println(rateOfInterest);
        System.out.println((rate));
    }
}
